// For Loop

let lotteryNumbers = [12, 45, 56, 54, 32, 60, 90, 100];

console.log("Tamanho do vetor lotteryNumbers:", lotteryNumbers.length);

for(let index = 0; index < lotteryNumbers.length; index += 1){  
  console.log(`Indice: ${index} Numero: ${lotteryNumbers[index]}`); // Template literals
};
